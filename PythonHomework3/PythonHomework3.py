# вводит натуральное число N – количество элементов в массиве. В последующих строках записаны N целых чисел Ai. Последняя строка
# содержит число X.

list_1 = []
n = int(input("Введите количество чисел: "))
for i in range(n):
    list_1.append(int(input("Введите число: ")))
x = int(input("Введите значение x: "))
count = 0
for i in range(len(list_1)):
    if list_1[i] < x:
        count = -count
    else:
        count = count + 0
    if list_1[i] >= x and list_1[i] - x <= count - x:
        count = list_1[i]
    elif list_1[i] <= x and count - x <= list_1[i] - x:
        count = list_1[i]
print(count)

