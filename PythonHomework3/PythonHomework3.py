if a[0].lower() in eng:
    sum = 0
    for i in a:
        for k, val in list_eng.items():
            if i.upper() in val:
                sum += k
    print(f"Стоимость введенного английского слова = {sum}")
else:
    if a[0].lower() in rus:
        sum = 0
        for i in a:
            for k, val in list_rus.items():
                if i.upper() in val:
                    sum += k
